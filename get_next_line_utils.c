#include "get_next_line.h"

char	*ft_strdup(const char *s)
{
	char	*cpy;
	int		i;
	int		len;

	len = 0;
	while (s[len])
		len++;
	cpy = (char *)malloc(sizeof(char) * (len + 1));
	if (cpy == NULL)
		return (NULL);
	i = 0;
	while (s[i])
	{
		cpy[i] = s[i];
		i++; 
	}
	cpy[i] = '\0';
	return (cpy);
}

size_t	ft_strlen(const char *s)
{
	size_t	i;

	i = 0;
	while (s[i])
		i++;
	return (i);
}

